namespace Witlesss.Commands;

public class SendMessage : SyncCommand
{
    private string _text = default!;

    public SendMessage WithText(string text)
    {
        _text = text;
        return this;
    }

    protected override void Run() => Bot.SendMessage(Chat, _text);
}

